package com.krakedev;

public class Fecha {
	// Atributos
	private int anio;
	private int mes;
	private int dia;

	// Metodos para leer
	public int getAnio() {
		return anio;
	}

	public int getMes() {
		return mes;
	}

	public int getDia() {
		return dia;
	}

	// Metodos para modificar
	public void setAnio(int anio) {
		this.anio = anio;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public void setDia(int dia) {
		this.dia = dia;
	}

}
